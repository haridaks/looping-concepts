package Looping;

public class DoWhileLoop {

	public static void main(String[] args) {
		int i=1;
		do {
			System.out.println("hello");
			i++;
		}
		while(i<=10);

	
	
	
	int k = 2;
	do {
		System.out.println("Hi");
		k++;
	}
	while(false); //even if the condition fails it will execute one time

}
}
